    private void cbSubActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbSubActionPerformed
        cbExamId.removeAllItems();
        boolean res=validateInput();
        if(!res)
        {
          JOptionPane.showMessageDialog(null,"Please Select the subject first","Information!",JOptionPane.INFORMATION_MESSAGE);
          return;
        }
        try
        {
          if(ExamDAO.isPaperSet(subName)==false)
          {
            JOptionPane.showMessageDialog(null,"Sorry no Exam Set for selected paper","Information!",JOptionPane.INFORMATION_MESSAGE);
          return;
          }
          ArrayList<String>examList=ExamDAO.getExamIdBySubject(UserProfile.getUsername(), subName);
          if(examList.isEmpty())
          {
            JOptionPane.showMessageDialog(null,"You have already appeared for the exam","Information!",JOptionPane.INFORMATION_MESSAGE);
          return;
          }
          
          for(String examId:examList)
          {
            cbExamId.addItem(examId);
          }
        }catch(Exception e)
        {
         JOptionPane.showMessageDialog(null,"ChooseTest Error","Error!",JOptionPane.ERROR_MESSAGE);
         e.printStackTrace();
        }
    }//GEN-LAST:event_cbSubActionPerformed

