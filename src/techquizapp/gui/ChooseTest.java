     private boolean validateInput()
   {
      int index=cbSub.getSelectedIndex();
         if(index==0)
         
           return false;
         subName=cbSub.getSelectedItem().toString();
         return true;

   } 
    private void btnTakeTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTakeTestActionPerformed
          boolean res=validateInput();
        if(!res)
        {
          JOptionPane.showMessageDialog(null,"Please Select the subject first","Information!",JOptionPane.INFORMATION_MESSAGE);
          return;
        }
        int count=cbExamId.getItemCount();
        if(count==0)
        {
        JOptionPane.showMessageDialog(null,"Please Select Exam","ERROR!",JOptionPane.ERROR_MESSAGE);
          return;
        }
        String examId=cbExamId.getSelectedItem().toString();
        int ans=JOptionPane.showConfirmDialog(null,"you have selected the subject "+subName+"of ExamID "+examId+"\nAre you sure you want to continue?","CONTINUE?",JOptionPane.INFORMATION_MESSAGE);
        if(ans==JOptionPane.YES_OPTION)
        {
          try
          {
            int totalQue=ExamDAO.getQuestionCountByExam(examId);
            Exam exam=new Exam(examId,subName,totalQue);
            TakeTest t1=new TakeTest(exam);
            t1.setVisible(true);
            this.dispose();
            
          }catch(SQLException e)
          {
          JOptionPane.showMessageDialog(null,"ChooseTest Error","Error!",JOptionPane.ERROR_MESSAGE);
          }
        
        
        }
    }//GEN-LAST:event_btnTakeTestActionPerformed

